#[derive(Parser, Debug)]
#[command(author, version, about, long_about = None)]
struct Args {
    #[arg(long, default_value = "0.0.0.0")]
    listen_host: String,

    #[arg(long, default_value_t = 8000)]
    listen_port: u16,
}

#[derive(Serialize)]
struct DbRowHex {
    id_hex: String,
    diag_hex: String,
    risk_hex: String,
}

#[derive(Deserialize)]
struct SearchPayload {
    receipt_bytes: Vec<u8>,
    encrypted_query_bytes: Vec<u8>,
}

/// Payload pentru Oblivious Write (Faza 3).
/// target_id_bytes: FheUint16 criptat — ID-ul pacientului țintă.
/// field: "diag" sau "risk" — câmpul de actualizat.
/// delta_bytes: FheUint16 criptat — valoarea nouă (înlocuire directă, nu adunare).
#[derive(Deserialize)]
struct UpdatePayload {
    receipt_bytes: Vec<u8>,
    target_id_bytes: Vec<u8>,
    field: String,
    new_value_bytes: Vec<u8>,
}

#[derive(Serialize)]
struct UpdateResponse {
    success: bool,
    error: Option<String>,
}

#[derive(Serialize, Deserialize)]
struct EncryptedRecord {
    pid: FheUint16,
    diag: FheUint16,
    risk: FheUint16,
}

/// AppState foloseste Mutex pe DB pentru scriere sigura concurenta (cerut de Axum + tokio).
struct AppState {
    server_key: ServerKey,
    db: Mutex<Vec<EncryptedRecord>>,
    commitment: Mutex<Option<[u8; 32]>>,
}

#[tokio::main]
async fn main() {
    let args = Args::parse();
    println!("[SERVER] Booting Untrusted Cloud Server...");
    println!("[SERVER] Waiting for shared_data/server_key.bin to be generated by Client...");
    
    let (server_key, db) = loop {
        if let (Ok(sk_data), Ok(db_data)) = (fs::read("shared_data/server_key.bin"), fs::read("shared_data/db.bin")) {
            let sk_res = bincode::deserialize::<ServerKey>(&sk_data);
            let db_res = bincode::deserialize::<Vec<EncryptedRecord>>(&db_data);
            
            if let (Ok(sk), Ok(db)) = (sk_res, db_res) {
                break (sk, db);
            }
        }
        std::thread::sleep(std::time::Duration::from_secs(1));
    };

    println!("[SERVER] Loading TFHE Keys and Encrypted Database...");
    tfhe::set_server_key(server_key.clone());
    
    // Incarcam commitment-ul daca exista deja pe disc (de la o sesiune anterioara)
    let initial_commitment = match fs::read("shared_data/commitment.bin") {
        Ok(c) if c.len() >= 32 => {
            let mut arr = [0u8; 32];
            arr.copy_from_slice(&c[0..32]);
            Some(arr)
        },
        _ => None,
    };

    let state = Arc::new(AppState {
        server_key,
        db: Mutex::new(db),
        commitment: Mutex::new(initial_commitment),
    });

    let app = Router::new()
        .route("/db_state", get(get_db_state))
        .route("/search", post(search_db))
        .route("/update", post(update_db))
        .route("/register", post(register_commitment))
        .layer(CorsLayer::permissive())
        .with_state(state);

    let bind_addr = format!("{}:{}", args.listen_host, args.listen_port);
    println!("[SERVER] ---> RUNNING ON {}", bind_addr);
    let listener = tokio::net::TcpListener::bind(&bind_addr).await.unwrap();
    axum::serve(listener, app).await.unwrap();
}

async fn register_commitment(
    State(state): State<Arc<AppState>>,
    Json(commitment_hex): Json<String>,
) -> impl IntoResponse {
    let bytes = match hex::decode(&commitment_hex) {
        Ok(b) => b,
        Err(_) => return (StatusCode::BAD_REQUEST, "Invalid hex encoding").into_response(),
    };
    if bytes.len() != 32 {
        return (StatusCode::BAD_REQUEST, "Commitment must be exactly 32 bytes").into_response();
    }
    let mut arr = [0u8; 32];
    arr.copy_from_slice(&bytes);
    *state.commitment.lock().unwrap() = Some(arr);
    println!("[SERVER] Registered new user commitment.");
    StatusCode::OK.into_response()
}

async fn get_db_state(State(state): State<Arc<AppState>>) -> Json<Vec<DbRowHex>> {
    let db = state.db.lock().unwrap_or_else(|e| e.into_inner());
    let mut rows = Vec::new();
    for rec in db.iter() {
        let id_bytes = bincode::serialize(&rec.pid).unwrap();
        let diag_bytes = bincode::serialize(&rec.diag).unwrap();
        let risk_bytes = bincode::serialize(&rec.risk).unwrap();
        rows.push(DbRowHex {
            id_hex: hex::encode(id_bytes),
            diag_hex: hex::encode(diag_bytes),
            risk_hex: hex::encode(risk_bytes),
        });
    }
    Json(rows)
}

/// Verifica ZKP receipt si compara commitment-ul din journal cu cel stocat.
fn verify_zkp(state: &AppState, receipt_bytes: &[u8]) -> Result<(), String> {
    let commitment = {
        let lock = state.commitment.lock().unwrap();
        match *lock {
            Some(c) => c,
            None => return Err("Server does not have user commitment registered.".to_string()),
        }
    };

    let receipt: Receipt = bincode::deserialize(receipt_bytes)
        .map_err(|_| "Invalid receipt encoding".to_string())?;

    if receipt.verify(METHOD_NAME_ID).is_err() {
        println!("[SERVER] FATAL: Fake STARK Proof detected. Dropping connection!");
        return Err("ZKP Verification Failed. Intrusion detected.".to_string());
    }

    let journal_commitment: [u8; 32] = receipt.journal.decode()
        .map_err(|_| "Journal decode failed".to_string())?;

    if journal_commitment != commitment {
        println!("[SERVER] FATAL: ZK Journal commitment mismatch!");
        return Err("Invalid Journal Commitment - Unauthorized".to_string());
    }

    println!("[SERVER] STARK Validated. Identity Confirmed via SHA-256 Hash.");
    Ok(())
}

async fn search_db(
    State(state): State<Arc<AppState>>,
    Json(payload): Json<SearchPayload>,
) -> impl IntoResponse {
    println!("\n[SERVER] =========================================");
    println!("[SERVER] INCOMING SEARCH REQUEST: ZKP + FHE PAYLOAD");

    if let Err(e) = verify_zkp(&state, &payload.receipt_bytes) {
        return (StatusCode::UNAUTHORIZED, e).into_response();
    }

    let query_id: FheUint16 = match bincode::deserialize(&payload.encrypted_query_bytes) {
        Ok(q) => q,
        Err(_) => return (StatusCode::BAD_REQUEST, "Invalid query encoding".to_string()).into_response(),
    };

    println!("[SERVER] Executing FHE Blind Search (Homomorphic Multiplexer)...");
    
    // Setam ServerKey in contextul thread-ului curent de Tokio, altfel FHE panicheaza
    let server_key_clone = state.server_key.clone();
    tfhe::set_server_key(server_key_clone.clone());
    
    // Multiplexer Homomorfic Parallels
    let db = state.db.lock().unwrap_or_else(|e| e.into_inner());
    let zero = FheUint16::encrypt_trivial(0u16);

    let result_accumulator = db.par_iter().map(|rec| {
        tfhe::set_server_key(server_key_clone.clone());
        let is_match = rec.pid.eq(&query_id);
        is_match.if_then_else(&rec.diag, &zero)
    }).reduce(|| {
        tfhe::set_server_key(server_key_clone.clone());
        zero.clone()
    }, |a, b| {
        tfhe::set_server_key(server_key_clone.clone());
        a + b
    });
    
    drop(db);

    println!("[SERVER] DB Operation Complete. Returning new LWE Ciphertext...");
    let result_bytes = bincode::serialize(&result_accumulator).unwrap();
    (StatusCode::OK, result_bytes).into_response()
}

/// Oblivious Write Multiplexer (Faza 3) — Inovatia algoritmicala principala.
/// Actualizeaza un camp din baza de date FARA sa stie ce rand s-a modificat.
/// Echivalentul unui ORAM (Oblivious RAM) homomorfic cu cost O(n) per scriere.
///
/// Formula matematica (LWE): new_val = old_val * (1 - match) + new_val * match
/// unde match = Enc(pid == target_id) — un bit criptat FHE
async fn update_db(
    State(state): State<Arc<AppState>>,
    Json(payload): Json<UpdatePayload>,
) -> impl IntoResponse {
    println!("\n[SERVER] =========================================");
    println!("[SERVER] INCOMING OBLIVIOUS WRITE REQUEST");

    if let Err(e) = verify_zkp(&state, &payload.receipt_bytes) {
        return Json(UpdateResponse { success: false, error: Some(e) }).into_response();
    }

    let target_id: FheUint16 = match bincode::deserialize(&payload.target_id_bytes) {
        Ok(t) => t,
        Err(_) => return Json(UpdateResponse {
            success: false,
            error: Some("Invalid target_id encoding".to_string()),
        }).into_response(),
    };

    let new_value: FheUint16 = match bincode::deserialize(&payload.new_value_bytes) {
        Ok(v) => v,
        Err(_) => return Json(UpdateResponse {
            success: false,
            error: Some("Invalid new_value encoding".to_string()),
        }).into_response(),
    };

    let field = payload.field.as_str();
    if field != "diag" && field != "risk" {
        return Json(UpdateResponse {
            success: false,
            error: Some("Invalid field. Use 'diag' or 'risk'.".to_string()),
        }).into_response();
    }

    println!("[SERVER] Executing Oblivious Write on field '{}' (touches ALL rows blindly)...", field);

    let server_key_clone = state.server_key.clone();
    tfhe::set_server_key(server_key_clone.clone());
    let mut db = state.db.lock().unwrap_or_else(|e| e.into_inner());

    db.par_iter_mut().for_each(|rec| {
        tfhe::set_server_key(server_key_clone.clone());
        let is_match = rec.pid.eq(&target_id);

        match field {
            "diag" => {
                rec.diag = is_match.if_then_else(&new_value, &rec.diag);
            },
            "risk" => {
                rec.risk = is_match.if_then_else(&new_value, &rec.risk);
            },
            _ => unreachable!(),
        }
    });

    // Salveaza DB-ul actualizat pe disc pentru persistenta
    match bincode::serialize(&*db) {
        Ok(bytes) => {
            if let Err(e) = fs::write("shared_data/db.bin", bytes) {
                eprintln!("[SERVER] WARN: Failed to persist DB to disk: {}", e);
            } else {
                println!("[SERVER] DB persisted to disk after Oblivious Write.");
            }
        },
        Err(e) => eprintln!("[SERVER] WARN: Failed to serialize DB: {}", e),
    }
    drop(db);

    println!("[SERVER] Oblivious Write complete. Zero information leaked about target row.");
    Json(UpdateResponse { success: true, error: None }).into_response()
}
